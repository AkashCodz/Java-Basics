package Attribute;

public class Multiple_attribue {
  String fname = "Akash";
  String lname = "Sharma";
  int age = 20;

  public static void main(String[] args) {
    Multiple_attribue myObj = new Multiple_attribue();
    System.out.println("Name: " + myObj.fname + " " + myObj.lname);
    System.out.println("Age: " + myObj.age);
  }
}